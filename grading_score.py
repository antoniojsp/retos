def gradingStudents(grades):
    # Write your code here
    lowest = 38
    multiple = 5
    maximum_diff = 3

    for i, j in enumerate(grades):
        if j >= lowest:  # "Any grade below 38 is ignored"
            diff = multiple - (j % multiple)  # look for diff between the grade and the next multiple of 5
            if diff < maximum_diff:
                grades[i] += diff  # changes the grade in the grade list
    return grades  # returns the grade modify


